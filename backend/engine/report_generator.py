# SORTING HELPER
# ──────────────────────────────────────────────────────────────

_SEV_ORDER = {"CRITICAL": 0, "HIGH": 1, "MEDIUM": 2, "LOW": 3}
_TYPE_ORDER = {"MISCONFIG": 0, "RECOMMENDATION": 1}


def _sort_findings(findings: list) -> list:
    """Sort findings: CRITICAL→HIGH→MEDIUM→LOW, then MISCONFIG before RECOMMENDATION."""
    def sort_key(f):
        sev = (f.get("severity") or "LOW").upper()
        ftype = (f.get("type") or "RECOMMENDATION").upper()
        return (_SEV_ORDER.get(sev, 9), _TYPE_ORDER.get(ftype, 9))
    return sorted(findings, key=sort_key)


