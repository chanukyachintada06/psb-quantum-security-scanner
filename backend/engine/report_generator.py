# PDF: FOOTER (drawn on every page)
# ──────────────────────────────────────────────────────────────

def _build_page_footer(canvas, doc, styles):
    """Draw persistent footer on every PDF page."""
    canvas.saveState()
    w, h = A4
    # Separator line
    canvas.setStrokeColor(BRAND_BORDER)
    canvas.setLineWidth(0.5)
    canvas.line(18 * mm, 18 * mm, w - 18 * mm, 18 * mm)
    # Footer text
    canvas.setFont("Helvetica", 7)
    canvas.setFillColor(colors.HexColor("#7F8090"))
    canvas.drawCentredString(w / 2, 12 * mm, FOOTER_TEXT)
    # Page number
    canvas.drawRightString(w - 18 * mm, 12 * mm, f"Page {doc.page}")
    # Confidential label
    canvas.drawString(18 * mm, 12 * mm, "CONFIDENTIAL")
    canvas.restoreState()


