def _build_findings_table(data: dict, styles) -> list:
    findings = _sort_findings(data.get("findings", []))

    if not findings:
        return [
            Paragraph("Findings & Recommendations", styles["SectionHeading"]),
            HRFlowable(width="100%", thickness=1, color=BRAND_BORDER, spaceAfter=4),
            Paragraph("No findings recorded for this scan.", styles["Narrative"]),
            Spacer(1, 8),
        ]

    rows = [
        [Paragraph("<b>Severity</b>", styles["TableHeader"]),
         Paragraph("<b>Type</b>", styles["TableHeader"]),
         Paragraph("<b>Title</b>", styles["TableHeader"]),
         Paragraph("<b>Description</b>", styles["TableHeader"])],
    ]

    for f in findings:
        sev   = (f.get("severity") or "LOW").upper()
        ftype = (f.get("type") or "RECOMMENDATION").upper()
        title = f.get("title") or "Security Notice"
        desc  = f.get("description") or ""

        sev_style = ParagraphStyle(
            f"sev_{sev}",
            parent=styles["TableCell"],
            textColor=SEVERITY_COLORS.get(sev, BRAND_TEXT_MUTED),
            fontName="Helvetica-Bold",
        )
        rows.append([
            Paragraph(sev, sev_style),
            Paragraph(ftype.replace("_", " "), styles["TableCell"]),
            Paragraph(title, ParagraphStyle("tf", parent=styles["TableCell"], fontName="Helvetica-Bold")),
            Paragraph(desc[:240] + ("…" if len(desc) > 240 else ""), styles["TableCell"]),
        ])

    # Alternating rows
    row_bgs = [BRAND_DARK]
    for i, f in enumerate(findings):
        sev = (f.get("severity") or "LOW").upper()
        row_bgs.append(
            SEVERITY_BG.get(sev, colors.white) if i % 2 == 0 else colors.white
        )

    t = Table(rows, colWidths=["12%", "16%", "24%", "48%"])
    t.setStyle(TableStyle([
        ("ROWBACKGROUNDS",  (0, 0), (-1, -1), row_bgs),
        ("TEXTCOLOR",       (0, 0), (-1, 0),  colors.white),
        ("FONTSIZE",        (0, 0), (-1, -1),  8),
        ("ALIGN",           (0, 0), (-1, -1),  "LEFT"),
        ("VALIGN",          (0, 0), (-1, -1),  "TOP"),
        ("TOPPADDING",      (0, 0), (-1, -1),  5),
        ("BOTTOMPADDING",   (0, 0), (-1, -1),  5),
        ("LEFTPADDING",     (0, 0), (-1, -1),  8),
        ("RIGHTPADDING",    (0, 0), (-1, -1),  8),
        ("BOX",             (0, 0), (-1, -1),  1, BRAND_BORDER),
        ("INNERGRID",       (0, 0), (-1, -1),  0.4, colors.HexColor("#E5E3E0")),
    ]))

    return [
        PageBreak(),
        Paragraph("Findings & Recommendations", styles["SectionHeading"]),
        HRFlowable(width="100%", thickness=1, color=BRAND_BORDER, spaceAfter=4),
        t,
        Spacer(1, 10),
    ]


