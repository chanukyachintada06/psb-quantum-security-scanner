def generate_pdf_report(data: dict) -> bytes:
    """
    Build and return a full enterprise-grade PDF report as bytes.

    Args:
        data: Unified scan dict returned by db.get_scan_for_report()

    Returns:
        bytes: Raw PDF content
    """
    buffer  = io.BytesIO()
    styles  = _build_styles()

    doc = SimpleDocTemplate(
        buffer,
        pagesize=A4,
        leftMargin=18 * mm,
        rightMargin=18 * mm,
        topMargin=18 * mm,
        bottomMargin=22 * mm,
        title=f"QPS Scan Report — {data.get('domain', 'unknown')}",
        author="Quantum Security Intelligence Engine",
        subject="Post-Quantum Cryptography Compliance Scan Report",
        creator="Team CypherRed261 | PSB Hackathon 2026",
    )

    story = []
    story += _build_pdf_header(data, styles)
    story += _build_executive_narrative(data, styles)
    story += _build_summary_section(data, styles)
    story += _build_compliance_snapshot(data, styles)
    story += _build_findings_table(data, styles)
    story += _build_node_analysis_table(data, styles)

