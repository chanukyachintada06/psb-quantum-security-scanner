    wb = Workbook()

    # ── Sheet 1: Summary ──────────────────────────────────────
    ws1 = wb.active
    ws1.title = "Summary"

    profile  = data.get("risk_profile", {})
    domain   = data.get("domain", "N/A")
    scan_id  = data.get("scan_id", "N/A")
    version  = data.get("scan_version", "v1.0")
    gen_at   = data.get("generated_at", datetime.datetime.utcnow().isoformat())
    gen_by   = data.get("generated_by", "Quantum Security Engine")

    # Title banner
    ws1["A1"] = "Quantum-Proof Systems Scanner — Scan Report"
    ws1["A1"].font = Font(bold=True, size=14, color=_XL_WHITE)
    ws1["A1"].fill = PatternFill("solid", fgColor=_XL_DARK)
    ws1.merge_cells("A1:D1")
    ws1["A1"].alignment = Alignment(horizontal="center", vertical="center")
    ws1.row_dimensions[1].height = 28

    ws1["A2"] = "Team CypherRed261 | PSB Hackathon 2026"
    ws1["A2"].font = Font(bold=False, size=9, color=_XL_WHITE)
    ws1["A2"].fill = PatternFill("solid", fgColor="1A1A2E")
    ws1.merge_cells("A2:D2")
    ws1["A2"].alignment = Alignment(horizontal="center")

    # Metadata fields
    s1_data = [
        ("", ""),
        ("Field", "Value"),
        ("Domain", domain),
        ("Scan ID", str(scan_id)),
        ("Scan Version", version),
        ("Generated At", gen_at[:19].replace("T", " ") + " UTC"),
        ("Generated By", gen_by),
        ("", ""),
        ("Risk Metric", "Value"),
        ("Overall Risk Level", profile.get("risk_level", "N/A")),
        ("PQC Compliance Score", f"{profile.get('pqc_score', 'N/A')} / 100"),
        ("Crypto Agility Score", f"{profile.get('crypto_agility_score', 'N/A')} / 100"),
        ("Cryptographic Mode", profile.get("crypto_mode", "N/A")),
        ("Quantum Risk Horizon", f"{profile.get('quantum_risk_horizon', 'N/A')} years"),
        ("HNDL Risk", "YES" if profile.get("hndl_risk") else "NO"),
        ("Engine Confidence", f"{profile.get('confidence_score', 'N/A')}%"),
        ("Scan Duration (ms)", str(data.get("scan_duration_ms", "N/A"))),
        ("", ""),
        ("Compliance", "Status"),
        ("NIST FIPS 203 (ML-KEM)", "Partial — Migration Recommended" if (profile.get("pqc_score") or 0) < 50 else "Aligned"),
        ("NIST FIPS 204 (ML-DSA)", "Partial — Migration Recommended" if (profile.get("pqc_score") or 0) < 50 else "Aligned"),
        ("NIST FIPS 205 (SLH-DSA)", "Partial — Migration Recommended" if (profile.get("pqc_score") or 0) < 50 else "Aligned"),
        ("RBI Cyber Security Framework", "Non-Compliant" if (profile.get("risk_level") or "").upper() in ("CRITICAL", "HIGH") else "Partial Compliance"),
        ("CERT-IN IT Act", "Non-Compliant" if (profile.get("risk_level") or "").upper() in ("CRITICAL", "HIGH") else "Partial Compliance"),
    ]

    border = _xl_thin_border()
    header_rows = {2, 9, 19}  # 0-indexed offset + 3 for start row
    sub_header_rows = {4, 11, 21}  # actual Excel row numbers for "section headers"

    for i, (label, value) in enumerate(s1_data):
        row = i + 3
        c_label = ws1.cell(row=row, column=1, value=label)
        c_value = ws1.cell(row=row, column=2, value=value)

        is_col_header = label in ("Field", "Risk Metric", "Compliance")
        is_empty = (label == "" and value == "")

        if is_col_header:
            for c in (c_label, c_value):
                c.font = Font(bold=True, size=9, color=_XL_WHITE)
                c.fill = PatternFill("solid", fgColor="333344")
                c.alignment = Alignment(horizontal="center", vertical="center")
                c.border = border
        elif is_empty:
            pass
        else:
            c_label.font = Font(bold=True, size=9)
            c_label.fill = PatternFill("solid", fgColor=_XL_LIGHT)
            c_label.border = border
            c_value.font = Font(size=9)
            c_value.alignment = Alignment(wrap_text=True, vertical="top")
            c_value.border = border

    ws1.column_dimensions["A"].width = 32
    ws1.column_dimensions["B"].width = 45

    # ── Sheet 2: IP Node Details ──────────────────────────────
    ws2 = wb.create_sheet("IP Node Details")
    node_headers = [
        "IP Address", "TLS Version", "Cipher Suite", "Key Exchange",
        "Key Type", "Key Size", "Cert Chain Status", "Scan Status", "Error"
    ]
    node_widths = [18, 12, 35, 28, 16, 14, 20, 14, 30]
    _xl_set_headers(ws2, node_headers, node_widths)

    nodes = data.get("ip_details", [])
    for i, node in enumerate(nodes, start=2):
        tls  = node.get("tls") or {}
        cert = node.get("certificate") or {}
        ok   = node.get("is_successful", True)
        fill = PatternFill("solid", fgColor="F0FDF4") if ok else PatternFill("solid", fgColor="FEF2F2")
        _xl_write_row(ws2, i, [
            node.get("ip_address", ""),
            tls.get("version", ""),
            tls.get("cipher_suite", ""),
            tls.get("key_exchange", ""),
            tls.get("public_key_type", ""),
            str(tls.get("key_size", "")),
            cert.get("chain_status", "UNKNOWN"),
            "SUCCESS" if ok else "FAILED",
            node.get("error_message", ""),
        ], fill=fill)

    # ── Sheet 3: Findings ─────────────────────────────────────
    ws3 = wb.create_sheet("Findings")
    finding_headers = ["Severity", "Type", "Title", "Description"]
    finding_widths  = [14, 18, 38, 65]
    _xl_set_headers(ws3, finding_headers, finding_widths)

    findings = _sort_findings(data.get("findings", []))
    for i, f in enumerate(findings, start=2):
        sev   = (f.get("severity") or "LOW").upper()
        fill  = _SEV_FILL.get(sev, PatternFill("solid", fgColor=_XL_LIGHT))
        row_vals = [
            sev,
            (f.get("type") or "RECOMMENDATION"),
            f.get("title", ""),
            f.get("description", ""),
        ]
        _xl_write_row(ws3, i, row_vals, fill=fill)
        # Severity cell bold + colored
        sev_cell = ws3.cell(row=i, column=1)
        sev_cell.font = Font(bold=True, color=_SEV_FONT_COLOR.get(sev, _XL_DARK), size=9)

    # ── Sheet 4: Recommendations ──────────────────────────────
    ws4 = wb.create_sheet("Recommendations")
    rec_headers = ["Priority", "Title", "Recommendation"]
    rec_widths  = [14, 38, 80]
    _xl_set_headers(ws4, rec_headers, rec_widths)

    recs = [f for f in findings if (f.get("type") or "").upper() == "RECOMMENDATION"]
    for i, f in enumerate(recs, start=2):
        sev  = (f.get("severity") or "LOW").upper()
        fill = _SEV_FILL.get(sev, PatternFill("solid", fgColor=_XL_LIGHT))
        _xl_write_row(ws4, i, [
            sev,
            f.get("title", ""),
            f.get("description", ""),
        ], fill=fill)
        ws4.cell(row=i, column=1).font = Font(bold=True, color=_SEV_FONT_COLOR.get(sev, _XL_DARK), size=9)

    # ── Serialize ─────────────────────────────────────────────
    buffer = io.BytesIO()
    wb.save(buffer)
    buffer.seek(0)
    return buffer.read()
