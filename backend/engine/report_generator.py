_SEV_FILL = {
    "CRITICAL": PatternFill("solid", fgColor="FEF2F2"),
    "HIGH":     PatternFill("solid", fgColor="FFF0F0"),
    "MEDIUM":   PatternFill("solid", fgColor="FFFBEB"),
    "LOW":      PatternFill("solid", fgColor="F0FDF4"),
}
